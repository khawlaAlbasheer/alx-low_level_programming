#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: int
 * @b: int
 * Return: value of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: int
 * @b: int
 * Return: subtraction result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two int
 * @a: int
 * @b: int
 * Return: multiplication of two int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two int
 * @a: int
 * @b: int
 * Return: division of two int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate mod
 * @a: int
 * @b: int
 * Return: a module b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
